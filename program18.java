//Java program to swap two numbers.


import java.util.*;

public class program18 {

    public static void main(String[] args) {

        int a = 5;
        int b = 8;
        int temp;

        temp = a;

         a = b;
         b = temp;

         System.out.println("a is storing:" +a);
         System.out.println("b is storing:" +b);
    }
}