// Java program to multiply the corresponding elements in the given two arrays

public class multiply {

        public static void main(String[] args) {
              
               int[] a = {1,2,3,4,5};
              
               int[] b = {5,4,3,2,1};
              
               for(int i=0;i<a.length;i++) {
                     
                     System.out.print(a[i]*b[i]+" ");
                     
              }
         
       }

}